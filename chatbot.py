def expand_synonyms(text: str) -> str:
    """Προσθέτει συνώνυμα στο τέλος του κειμένου."""
    words = normalise(text).split()
    expansions = []
    for w in words:
        if w in SYNONYMS:
            expansions.append(SYNONYMS[w])
        # Also check the normalised version
        w_norm = strip_accents(w.lower())
        if w_norm in SYNONYMS and SYNONYMS[w_norm] not in expansions:
            expansions.append(SYNONYMS[w_norm])
    if expansions:
        return text + " " + " ".join(expansions)
    return text


