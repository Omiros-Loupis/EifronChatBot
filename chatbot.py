class FAQEngine:
    def __init__(self, faq_list):
        self.faq = faq_list
        # Συνδυάζουμε question + keywords για καλύτερο matching
        self.corpus = [
            f"{item['question']} {item.get('keywords', '')}"
            for item in self.faq
        ]
        self.vectorizer = TfidfVectorizer(
            analyzer="word",
            ngram_range=(1, 2),
            min_df=1,
            sublinear_tf=True,
            stop_words=GREEK_STOPWORDS,
        )
        self.tfidf_matrix = self.vectorizer.fit_transform(self.corpus)

    def search(self, query: str, top_k: int = 3):
        query_vec = self.vectorizer.transform([query])
        scores = cosine_similarity(query_vec, self.tfidf_matrix).flatten()
        top_indices = scores.argsort()[::-1][:top_k]

        results = []
        for idx in top_indices:
            results.append({
                "score": float(scores[idx]),
                "category": self.faq[idx]["category"],
                "subcategory": self.faq[idx].get("subcategory"),
                "question": self.faq[idx]["question"],
                "answer": self.faq[idx]["answer"],
            })
        return results


