@app.route("/api/navigate", methods=["GET"])
def navigate():
    """
    Product-based navigation.
    - No params → product categories
    - ?product=X → questions in product
    - ?id=N → specific answer
    """
    faq_id = request.args.get("id")
    if faq_id is not None:
        try:
            idx = int(faq_id)
        except (TypeError, ValueError):
            return jsonify({"error": "Invalid ID"}), 400
        if 0 <= idx < len(FAQ):
            item = FAQ[idx]
            sev = item.get("severity", "blue")
            return jsonify({
                "type": "answer",
                "question": item["question"],
                "answer": item["answer"],
                "product": item["product"],
                "severity": sev,
                "severity_label": SEVERITY_META.get(sev, {}).get("label", ""),
                "severity_icon": SEVERITY_META.get(sev, {}).get("icon", "🔵"),
            })
        return jsonify({"error": "Invalid ID"}), 404

    product = request.args.get("product")

    if not product:
        # Return product categories
        products = []
        for prod in NAVIGATION_TREE:
            products.append({
                "name": prod,
                "icon": PRODUCT_ICONS.get(prod, "📋"),
                "count": len(NAVIGATION_TREE[prod]),
            })
        return jsonify({"type": "products", "items": products})

    if product not in NAVIGATION_TREE:
        return jsonify({"error": "Product not found"}), 404

    questions = NAVIGATION_TREE[product]
    return jsonify({
        "type": "questions",
        "product": product,
        "icon": PRODUCT_ICONS.get(product, "📋"),
        "items": questions,
    })


@app.route("/api/chat", methods=["POST"])
def chat():
    """Κύριο endpoint — δέχεται { "message": "...", "session_id": "..." }."""
    try:
        data = request.get_json(force=False, silent=False)
    except BadRequest:
        return jsonify({"error": "Invalid JSON body."}), 400

    if not isinstance(data, dict):
        return jsonify({"error": "Expected a JSON object body."}), 400

    user_message = str(data.get("message", "")).strip()
    session_id = str(data.get("session_id", "")).strip() or None

    if not user_message:
        return jsonify({"error": "Παρακαλώ εισάγετε ένα μήνυμα."}), 400

    if len(user_message) > 500:
        return jsonify({"error": "Το μήνυμα δεν μπορεί να υπερβαίνει τους 500 χαρακτήρες."}), 400

    # ── Χειρισμός χαιρετισμών / ευχαριστιών ──
    msg_norm = normalise(user_message)
    GREETINGS = ["γεια", "γεια σας", "γεια σου", "καλημερα", "καλησπερα", "καλο απογευμα", "hey", "hello", "hi"]
    THANKS = ["ευχαριστω", "thanks", "thank you", "σε ευχαριστω", "σας ευχαριστω", "ευχαριστουμε", "ok ευχαριστω"]

    if msg_norm in GREETINGS or any(msg_norm.startswith(g) for g in GREETINGS):
        return jsonify({
            "answer": "Γεια σας! 👋 Είμαι ο βοηθός υποστήριξης της Eifron.\n\nΜπορώ να σας βοηθήσω με ερωτήσεις σχετικά με την αντλία S9, το EasyPatch, τον αισθητήρα CGM και το PDM.\n\nΧρησιμοποιήστε την «Περιήγηση» για να βρείτε το θέμα σας ή γράψτε την ερώτησή σας!",
            "matched_question": None, "product": None,
            "confidence": 1.0, "is_fallback": False, "is_greeting": True,
        })

    if msg_norm in THANKS or any(msg_norm.startswith(t) for t in THANKS):
        return jsonify({
            "answer": "Παρακαλώ! 😊 Αν χρειαστείτε κάτι ακόμα, είμαι εδώ.\n\nΓια θέματα που δεν μπορώ να καλύψω:\n📱 Τεχνική Υποστήριξη (24/7): " + CONTACT_INFO['phone_support'] + "\n📞 Τηλεφωνικό Κέντρο: " + CONTACT_INFO['phone'],
            "matched_question": None, "product": None,
            "confidence": 1.0, "is_fallback": False, "is_greeting": True,
        })

    # ── 🎯 Exact match shortcut: όταν ο χρήστης κάνει click σε disambiguation
    #    option, το text έρχεται verbatim. Παράκαμψη fuzzy search.
    exact_match = QUESTION_LOOKUP.get(msg_norm)
    if exact_match:
        item = exact_match
        track_chat(session_id, item["question"], item["product"], False)
        set_session(session_id, item["question"], item["answer"], item["product"])
        sev = item.get("severity", "blue")
        return jsonify({
            "answer": item["answer"],
            "matched_question": item["question"],
            "product": item["product"],
            "severity": sev,
            "severity_label": SEVERITY_META.get(sev, {}).get("label", ""),
            "severity_icon": SEVERITY_META.get(sev, {}).get("icon", "🔵"),
            "confidence": 1.0,
            "related_questions": [],
            "is_disambiguation": False,
            "is_fallback": False,
        })

    # ── 🧠 Session Context: αν μικρό μήνυμα, εμπλούτισε από ιστορικό ──
    search_query = user_message
    session = get_session(session_id) if session_id else None
    if session and len(msg_norm.split()) <= 6:
        # Σύντομη follow-up → εμπλούτισε με context από προηγούμενο
        context_hint = f" {session['last_product']} {session['last_q']}"
        search_query = user_message + context_hint

    results = engine.search(search_query, top_k=3)
    best = results[0]

    # 1. ΥΨΗΛΗ ΣΙΓΟΥΡΙΑ (>45%)
    if best["score"] >= 0.45:
        related = [
            {
                "question": r["question"], "product": r["product"],
                "severity": r["severity"], "severity_icon": r["severity_icon"],
                "confidence": round(r["score"], 3),
            }
            for r in results[1:] if r["score"] >= 0.20
        ]
        # Track & store session
        track_chat(session_id, best["question"], best["product"], False)
        set_session(session_id, best["question"], best["answer"], best["product"])

        return jsonify({
            "answer": best["answer"],
            "matched_question": best["question"],
            "product": best["product"],
            "severity": best["severity"],
            "severity_label": best["severity_label"],
            "severity_icon": best["severity_icon"],
            "confidence": round(best["score"], 3),
            "related_questions": related,
            "is_disambiguation": False, "is_fallback": False,
        })

    # 2. ΜΕΤΡΙΑ ΣΙΓΟΥΡΙΑ (15%–44%): disambiguation
    elif best["score"] >= 0.15:
        track_chat(session_id, None, best.get("product"), False)
        # Store best guess in session for follow-up context
        set_session(session_id, best["question"], best["answer"], best.get("product", ""))
        return jsonify({
            "answer": "Βρήκα μερικά σχετικά θέματα, αλλά θέλω να είμαι σίγουρος. **Μήπως εννοείτε κάτι από τα παρακάτω;** 👇",
            "options": results,
            "is_disambiguation": True, "is_fallback": False,
            "confidence": round(best["score"], 3)
        })

    # 3. ΧΑΜΗΛΗ ΣΙΓΟΥΡΙΑ (<15%): Fallback
    else:
        track_chat(session_id, None, None, True)
        log_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "unanswered.log")
        try:
            with open(log_path, "a", encoding="utf-8") as f:
                f.write(f"{datetime.now().strftime('%Y-%m-%d %H:%M:%S')} - [{best['score']:.3f}] {user_message}\n")
        except OSError:
            pass
        return jsonify({
            "answer": FALLBACK_MESSAGE,
            "matched_question": None, "product": None,
            "is_disambiguation": False,
            "confidence": round(best["score"], 3),
            "is_fallback": True,
        })


