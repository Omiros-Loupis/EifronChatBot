# 🧠 Session Context (Μνήμη Ιστορικού)
# ─────────────────────────────────────────────
SESSION_STORE = {}   # {session_id: {"last_q": ..., "last_a": ..., "last_product": ..., "ts": ...}}
SESSION_TTL = 1800   # 30 λεπτά

def get_session(sid):
    if sid and sid in SESSION_STORE:
        s = SESSION_STORE[sid]
        if time.time() - s["ts"] < SESSION_TTL:
            return s
        else:
            del SESSION_STORE[sid]
    return None

def set_session(sid, question, answer, product):
    if not sid:
        return
    SESSION_STORE[sid] = {
        "last_q": question, "last_a": answer,
        "last_product": product, "ts": time.time()
    }
    # Cleanup old sessions (every 100 writes)
    if len(SESSION_STORE) > 200:
        cutoff = time.time() - SESSION_TTL
        expired = [k for k, v in SESSION_STORE.items() if v["ts"] < cutoff]
        for k in expired:
            del SESSION_STORE[k]


