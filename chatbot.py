class FAQEngine:
    def __init__(self, faq_list):
        self.faq = faq_list

        # Normalised corpus: question + keywords (accent-stripped, lowercase)
        self.corpus = [
            normalise(f"{item['question']} {item.get('keywords', '')} {item['answer']}")
            for item in self.faq
        ]

        # Word-level TF-IDF
        self.word_vec = TfidfVectorizer(
            analyzer="word",
            ngram_range=(1, 2),
            min_df=1,
            sublinear_tf=True,
            stop_words=GREEK_STOPWORDS,
        )
        self.word_matrix = self.word_vec.fit_transform(self.corpus)

        # Char-level TF-IDF for typo tolerance
        self.char_vec = TfidfVectorizer(
            analyzer="char_wb",
            ngram_range=(3, 5),
            min_df=1,
            sublinear_tf=True,
            max_features=30000,
        )
        self.char_matrix = self.char_vec.fit_transform(self.corpus)

        # ── Build intent index ──
        # Resolve each intent rule's question_substring to FAQ indices
        self.intent_rules_resolved = []
        for keywords, q_substr, boost in INTENT_RULES:
            q_substr_norm = normalise(q_substr)
            matched_indices = []
            for idx, item in enumerate(self.faq):
                q_norm = normalise(item["question"])
                if q_substr_norm in q_norm:
                    matched_indices.append(idx)
            if matched_indices:
                self.intent_rules_resolved.append(
                    ({normalise(k) for k in keywords}, matched_indices, boost)
                )

    def _compute_intent_scores(self, query_norm: str) -> np.ndarray:
        """Compute intent-based scores from co-occurrence rules."""
        scores = np.zeros(len(self.faq))
        query_words = set(query_norm.split())

        for keywords, faq_indices, boost in self.intent_rules_resolved:
            # All keywords must appear in the query
            if keywords.issubset(query_words):
                for idx in faq_indices:
                    scores[idx] = max(scores[idx], boost)

        return scores

    def search(self, raw_query: str, top_k: int = 3):
        # Normalise + expand synonyms
        expanded = expand_synonyms(raw_query)
        query_norm = normalise(expanded)

        if not query_norm.strip():
            return [{"score": 0.0, "product": "", "severity": "", "question": "", "answer": FALLBACK_MESSAGE}]

        # Word-level scoring
        wq = self.word_vec.transform([query_norm])
        word_scores = cosine_similarity(wq, self.word_matrix).flatten()

        # Char-level scoring (for typo tolerance)
        cq = self.char_vec.transform([query_norm])
        char_scores = cosine_similarity(cq, self.char_matrix).flatten()

        # Intent-based scoring (co-occurrence patterns)
        intent_scores = self._compute_intent_scores(query_norm)

        # Blended score: 50% word + 25% char + 25% intent
        # Intent acts as strong boost when matched, 0 otherwise
        scores = 0.50 * word_scores + 0.25 * char_scores + 0.25 * intent_scores

        # Where intent matched strongly, ensure minimum score
        # (prevents low TF-IDF from drowning out a strong intent match)
        intent_mask = intent_scores >= 0.30
        scores[intent_mask] = np.maximum(scores[intent_mask], intent_scores[intent_mask] * 0.8)

        top_indices = scores.argsort()[::-1][:top_k]

        results = []
        for idx in top_indices:
            sev = self.faq[idx].get("severity", "blue")
            results.append({
                "score": float(scores[idx]),
                "product": self.faq[idx]["product"],
                "severity": sev,
                "severity_label": SEVERITY_META.get(sev, {}).get("label", ""),
                "severity_icon": SEVERITY_META.get(sev, {}).get("icon", "🔵"),
                "question": self.faq[idx]["question"],
                "answer": self.faq[idx]["answer"],
            })
        return results


