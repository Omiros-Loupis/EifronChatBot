@app.route("/api/navigate", methods=["GET"])
def navigate():
    """
    Guided navigation tree.
    - No params → returns categories
    - ?category=X → returns subcategories
    - ?category=X&subcategory=Y → returns questions
    - ?id=N → returns specific answer
    """
    faq_id = request.args.get("id")
    if faq_id is not None:
        try:
            idx = int(faq_id)
        except (TypeError, ValueError):
            return jsonify({"error": "Invalid ID"}), 400
        if 0 <= idx < len(FAQ):
            item = FAQ[idx]
            return jsonify({
                "type": "answer",
                "question": item["question"],
                "answer": item["answer"],
                "category": item["category"],
                "subcategory": item["subcategory"],
            })
        return jsonify({"error": "Invalid ID"}), 404

    category = request.args.get("category")
    subcategory = request.args.get("subcategory")

    if not category:
        # Return top-level categories
        cats = []
        for cat in NAVIGATION_TREE:
            total = sum(len(v) for v in NAVIGATION_TREE[cat].values())
            cats.append({
                "name": cat,
                "icon": CATEGORY_ICONS.get(cat, "📋"),
                "count": total,
            })
        return jsonify({"type": "categories", "items": cats})

    if category and not subcategory:
        # Return subcategories
        if category not in NAVIGATION_TREE:
            return jsonify({"error": "Category not found"}), 404
        subs = []
        for sub, questions in NAVIGATION_TREE[category].items():
            subs.append({
                "name": sub,
                "icon": SUBCATEGORY_ICONS.get(sub, "📋"),
                "count": len(questions),
            })
        return jsonify({"type": "subcategories", "category": category, "items": subs})

    if category and subcategory:
        # Return questions in subcategory
        if category not in NAVIGATION_TREE or subcategory not in NAVIGATION_TREE[category]:
            return jsonify({"error": "Not found"}), 404
        questions = NAVIGATION_TREE[category][subcategory]
        return jsonify({
            "type": "questions",
            "category": category,
            "subcategory": subcategory,
            "items": questions,
        })


@app.route("/api/chat", methods=["POST"])
def chat():
    """Κύριο endpoint — δέχεται { "message": "..." } και επιστρέφει απάντηση."""
    try:
        data = request.get_json(force=False, silent=False)
    except BadRequest:
        return jsonify({"error": "Invalid JSON body."}), 400

    if not isinstance(data, dict):
        return jsonify({"error": "Expected a JSON object body."}), 400

    user_message = str(data.get("message", "")).strip()

    if not user_message:
        return jsonify({"error": "Παρακαλώ εισάγετε ένα μήνυμα."}), 400

    if len(user_message) > 500:
        return jsonify({"error": "Το μήνυμα δεν μπορεί να υπερβαίνει τους 500 χαρακτήρες."}), 400

    # ── Χειρισμός χαιρετισμών / ευχαριστιών ──
    msg_lower = user_message.lower().strip()
    GREETINGS = ["γεια", "γεια σας", "γεια σου", "καλημέρα", "καλησπέρα", "καλό απόγευμα", "hey", "hello", "hi"]
    THANKS = ["ευχαριστώ", "ευχαριστω", "thanks", "thank you", "σε ευχαριστώ", "σας ευχαριστώ", "ευχαριστούμε", "ok ευχαριστώ"]

    if msg_lower in GREETINGS or any(msg_lower.startswith(g) for g in GREETINGS):
        return jsonify({
            "answer": "Γεια σας! 👋 Είμαι ο βοηθός υποστήριξης της Eifron.\n\nΜπορώ να σας βοηθήσω με ερωτήσεις σχετικά με την αντλία S9, το EasyPatch και τον αισθητήρα CGM.\n\nΧρησιμοποιήστε την «Περιήγηση» για να βρείτε το θέμα σας ή γράψτε την ερώτησή σας!",
            "matched_question": None,
            "category": None,
            "confidence": 1.0,
            "is_fallback": False,
            "is_greeting": True,
        })

    if msg_lower in THANKS or any(msg_lower.startswith(t) for t in THANKS):
        return jsonify({
            "answer": "Παρακαλώ! 😊 Αν χρειαστείτε κάτι ακόμα, είμαι εδώ.\n\nΓια θέματα που δεν μπορώ να καλύψω:\n📱 Τεχνική Υποστήριξη (24/7): " + CONTACT_INFO['phone_support'] + "\n📞 Τηλεφωνικό Κέντρο: " + CONTACT_INFO['phone'],
            "matched_question": None,
            "category": None,
            "confidence": 1.0,
            "is_fallback": False,
            "is_greeting": True,
        })

    results = engine.search(user_message, top_k=3)
    best = results[0]

    if best["score"] >= CONFIDENCE_THRESHOLD:
        related = [
            {
                "question": r["question"],
                "category": r["category"],
                "subcategory": r.get("subcategory"),
                "confidence": round(r["score"], 3),
            }
            for r in results[1:]
            if r["score"] >= CONFIDENCE_THRESHOLD * 0.6
        ]
        return jsonify({
            "answer": best["answer"],
            "matched_question": best["question"],
            "category": best["category"],
            "subcategory": best.get("subcategory"),
            "confidence": round(best["score"], 3),
            "related_questions": related,
            "is_fallback": False,
        })
    else:
        # Log unanswered question
        with open("unanswered.log", "a", encoding="utf-8") as f:
            f.write(f"{datetime.now().strftime('%Y-%m-%d %H:%M:%S')} - [{best['score']:.3f}] {user_message}\n")
        return jsonify({
            "answer": FALLBACK_MESSAGE,
            "matched_question": None,
            "category": None,
            "confidence": round(best["score"], 3),
            "is_fallback": True,
        })



