STATS_LOCK = threading.Lock()

def track_chat(session_id, matched_question, product, is_fallback):
    with STATS_LOCK:
        today = datetime.now().strftime("%Y-%m-%d")
        if today != STATS["today_date"]:
            STATS["today_sessions"] = set()
            STATS["today_date"] = today
        STATS["total_chats"] += 1
        if session_id:
            STATS["total_sessions"].add(session_id)
            STATS["today_sessions"].add(session_id)
        if is_fallback:
            STATS["fallback_count"] += 1
        elif matched_question:
            STATS["questions_asked"][matched_question] += 1
        if product:
            STATS["products_asked"][product] += 1


